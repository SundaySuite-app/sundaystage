/**
 * i18n — Phase 13.1.
 *
 * A tiny, dependency-light translation layer: a per-language catalog, a `t()`
 * that falls back to English for any missing key, and a persisted locale store.
 * All seven locales (no, en, sv, da, de, fr, pl — matching SundayRec) are now
 * at full key parity with English; `i18n.test.ts` enforces that parity. `en`
 * remains the fallback for any key that might slip through.
 *
 * The machinery is what matters here: every user-visible string can go through
 * `t()`, and adding a language is just another catalog entry.
 */

import { create } from "zustand";

export type Lang = "no" | "en" | "sv" | "da" | "de" | "fr" | "pl";

export const LANGS: Lang[] = ["no", "en", "sv", "da", "de", "fr", "pl"];

/** Each language's name in its own tongue (endonym). Used wherever a language
 *  is picked from a list — the label reads naturally regardless of the current
 *  UI locale, so it needs no per-locale translation. */
const LANG_ENDONYMS: Record<Lang, string> = {
  no: "Norsk",
  en: "English",
  sv: "Svenska",
  da: "Dansk",
  de: "Deutsch",
  fr: "Français",
  pl: "Polski",
};

/** The endonym for a language code, falling back to the raw code. */
export function langLabel(l: string): string {
  return (LANG_ENDONYMS as Record<string, string>)[l] ?? l;
}

